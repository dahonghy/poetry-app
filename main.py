# ========== 填空检测界面 ==========
class FillTestScreen(Screen):
    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        self.answer_entries = []
        self.correct_answers = []
    
    def setup_test(self, poetry_name):
        data = BUILTIN_POETRY.get(poetry_name, {})
        content = data.get('content', '')
        
        self.clear_widgets()
        self.answer_entries = []
        self.correct_answers = []
        
        layout = BoxLayout(orientation='vertical', padding=dp(15), spacing=dp(10))
        
        layout.add_widget(Label(
            text=f'填空检测：{poetry_name}',
            font_name=FONT,
            font_size='18sp',
            color=COLORS['primary'],
            size_hint_y=0.08
        ))
        
        scroll = ScrollView(size_hint_y=0.8)
        q_layout = BoxLayout(orientation='vertical', size_hint_y=None, spacing=dp(10))
        q_layout.bind(minimum_height=q_layout.setter('height'))
        
        sentences = [s.strip() for s in content.split('\n') if s.strip() and len(s.strip()) > 4]
        random.shuffle(sentences)
        
        for i, sentence in enumerate(sentences[:5]):
            mid = len(sentence) // 2
            is_front = random.choice([True, False])
            
            if is_front:
                blank = sentence[:mid]
                show = sentence[mid:]
            else:
                show = sentence[:mid]
                blank = sentence[mid:]
            
            # 计算实际需要填写的字数
            blank_len = len(blank)
            blank_cn = num_to_cn(blank_len)
            
            if is_front:
                hint = f'______（需填{blank_cn}字） {show}'
            else:
                hint = f'{show} ______（需填{blank_cn}字）'
            
            q_box = BoxLayout(orientation='vertical', size_hint_y=None, height=dp(120))
            q_label = Label(
                text=f'第{NUM_CN[i+1]}题：{hint}',
                font_name=FONT,
                color=COLORS['text_dark'],
                font_size='15sp',
                size_hint_y=None,
                height=dp(60),
                halign='left',
                valign='middle',
                text_size=(None, None)
            )
            q_label.bind(
                width=lambda ins, w: setattr(ins, 'text_size', (w, None))
            )
            q_box.add_widget(q_label)
            
            entry = TextInput(
                hint_text='填写答案',
                font_name=FONT,
                font_size='16sp',
                size_hint_y=None,
                height=dp(45),
                multiline=False,
                background_color=COLORS['secondary']
            )
            q_box.add_widget(entry)
            
            self.answer_entries.append(entry)
            self.correct_answers.append(blank)
            q_layout.add_widget(q_box)
        
        scroll.add_widget(q_layout)
        layout.add_widget(scroll)
        
        btn_layout = BoxLayout(size_hint_y=0.12, spacing=dp(10))
        
        btn_submit = Button(
            text='提交',
            font_name=FONT,
            background_color=COLORS['primary']
        )
        btn_submit.bind(on_press=self.submit_answers)
        btn_layout.add_widget(btn_submit)
        
        btn_answer = Button(
            text='答案',
            font_name=FONT,
            background_color=COLORS['accent']
        )
        btn_answer.bind(on_press=self.show_answers)
        btn_layout.add_widget(btn_answer)
        
        btn_back = Button(
            text='返回',
            font_name=FONT,
            background_color=(0.6, 0.6, 0.6, 1)
        )
        btn_back.bind(on_press=self.go_back)
        btn_layout.add_widget(btn_back)
        layout.add_widget(btn_layout)
        
        self.add_widget(layout)
    
    def submit_answers(self, instance):
        correct = 0
        total = len(self.answer_entries)
        
        for entry, answer in zip(self.answer_entries, self.correct_answers):
            user = entry.text.strip().replace('，', '').replace('。', '').replace(' ', '')
            ans = answer.replace('，', '').replace('。', '').replace(' ', '')
            
            if user == ans:
                correct += 1
                entry.background_color = COLORS['success']
            else:
                entry.background_color = COLORS['danger']
        
        score = (correct / total * 100) if total > 0 else 0
        
        main_screen = self.manager.get_screen('main')
        name = main_screen.current_poetry
        if name not in main_screen.records:
            main_screen.records[name] = []
        main_screen.records[name].append({
            'type': '填空检测',
            'score': score,
            'time': f"{num_to_cn(datetime.now().month)}月{num_to_cn(datetime.now().day)}日"
        })
        main_screen.save_data()
        
        # 创建得分显示内容 - 使用中文数字
        score_int = int(score)
        score_cn = num_to_cn(score_int) if score_int <= 100 else str(score_int)
        correct_cn = num_to_cn(correct)
        total_cn = num_to_cn(total)
        
        content_layout = BoxLayout(orientation='vertical', padding=dp(20))
        score_label = Label(
            text=f'得分：{score_cn}分\n正确：{correct_cn}题，共{total_cn}题',
            font_name=FONT,
            color=COLORS['text_dark'],
            font_size='22sp',
            halign='center',
            valign='middle'
        )
        content_layout.add_widget(score_label)
        
        Popup(
            title='检测结果',
            title_font=FONT,
            title_color=COLORS['text_dark'],
            content=content_layout,
            size_hint=(0.75, 0.4),
            background='',
            background_color=COLORS['white']
        ).open()
    
    def show_answers(self, instance):
        for entry, answer in zip(self.answer_entries, self.correct_answers):
            entry.text = answer
            entry.background_color = COLORS['secondary']
    
    def go_back(self, instance):
        self.manager.current = 'main'

# ========== 全文背诵检测界面 ==========
class FullTestScreen(Screen):
    def __init__(self, **kwargs):
        super().__init__(**kwargs)
        self.poetry_name = ''
        self.original_content = ''
    
    def setup_test(self, poetry_name):
        data = BUILTIN_POETRY.get(poetry_name, {})
        self.poetry_name = poetry_name
        self.original_content = data.get('content', '')
        
        self.clear_widgets()
        
        layout = BoxLayout(orientation='vertical', padding=dp(15), spacing=dp(10))
        
        # 标题
        layout.add_widget(Label(
            text=f'全文背诵：{poetry_name}',
            font_name=FONT,
            font_size='18sp',
            color=COLORS['primary'],
            size_hint_y=0.08
        ))
        
        # 提示
        layout.add_widget(Label(
            text='请在下方输入全文内容：',
            font_name=FONT,
            font_size='14sp',
            color=COLORS['text_light'],
            size_hint_y=0.05,
            halign='left'
        ))
        
        # 文本输入框
        self.text_input = TextInput(
            hint_text='在此输入全文...',
            font_name=FONT,
            font_size='16sp',
            size_hint_y=0.65,
            multiline=True,
            background_color=COLORS['secondary'],
            foreground_color=COLORS['text_dark']
        )
        layout.add_widget(self.text_input)
        
        # 按钮
        btn_layout = BoxLayout(size_hint_y=0.12, spacing=dp(10))
        
        btn_submit = Button(
            text='提交检测',
            font_name=FONT,
            background_color=COLORS['primary']
        )
        btn_submit.bind(on_press=self.submit_full)
        btn_layout.add_widget(btn_submit)
        
        btn_show = Button(
            text='查看原文',
            font_name=FONT,
            background_color=COLORS['accent']
        )
        btn_show.bind(on_press=self.show_original)
        btn_layout.add_widget(btn_show)
        
        btn_back = Button(
            text='返回',
            font_name=FONT,
            background_color=(0.6, 0.6, 0.6, 1)
        )
        btn_back.bind(on_press=self.go_back)
        btn_layout.add_widget(btn_back)
        
        layout.add_widget(btn_layout)
        self.add_widget(layout)
    
    def submit_full(self, instance):
        user_text = self.text_input.text.strip()
        original = self.original_content.strip()
        
        if not user_text:
            Popup(
                title='提示',
                title_font=FONT,
                title_color=COLORS['text_dark'],
                content=Label(
                    text='请输入全文内容',
                    font_name=FONT,
                    color=COLORS['text_dark']
                ),
                size_hint=(0.6, 0.25),
                background='',
                background_color=COLORS['white']
            ).open()
            return
        
        # 统计
        original_clean = original.replace('，', '').replace('。', '').replace('？', '').replace('！', '').replace('、', '').replace(' ', '').replace('\n', '')
        user_clean = user_text.replace('，', '').replace('。', '').replace('？', '').replace('！', '').replace('、', '').replace(' ', '').replace('\n', '')
        
        # 计算相似度
        import difflib
        similarity = difflib.SequenceMatcher(None, original_clean, user_clean).ratio() * 100
        
        # 统计正确字数
        correct_chars = sum(1 for a, b in zip(original_clean, user_clean) if a == b)
        total_chars = len(original_clean)
        
        # 保存记录
        main_screen = self.manager.get_screen('main')
        if self.poetry_name not in main_screen.records:
            main_screen.records[self.poetry_name] = []
        main_screen.records[self.poetry_name].append({
            'type': '全文背诵',
            'score': similarity,
            'time': f"{num_to_cn(datetime.now().month)}月{num_to_cn(datetime.now().day)}日"
        })
        main_screen.save_data()
        
        # 显示结果 - 使用中文数字
        score_int = int(similarity)
        score_cn = num_to_cn(score_int) if score_int <= 100 else str(score_int)
        correct_cn = num_to_cn(correct_chars) if correct_chars <= 100 else str(correct_chars)
        total_cn = num_to_cn(total_chars) if total_chars <= 100 else str(total_chars)
        
        content_layout = BoxLayout(orientation='vertical', padding=dp(20), spacing=dp(10))
        
        score_text = f'相似度：{score_cn}%\n正确字数：{correct_cn}字，共{total_cn}字'
        if similarity >= 95:
            score_text += '\n\n太棒了！背诵非常准确！'
        elif similarity >= 80:
            score_text += '\n\n不错！继续加油！'
        elif similarity >= 60:
            score_text += '\n\n还需努力，多读几遍！'
        else:
            score_text += '\n\n加油！建议先熟读原文。'
        
        content_layout.add_widget(Label(
            text=score_text,
            font_name=FONT,
            color=COLORS['text_dark'],
            font_size='20sp',
            halign='center',
            valign='middle'
        ))
        
        Popup(
            title='背诵结果',
            title_font=FONT,
            title_color=COLORS['text_dark'],
            content=content_layout,
            size_hint=(0.8, 0.5),
            background='',
            background_color=COLORS['white']
        ).open()
    
    def show_original(self, instance):
        content_layout = BoxLayout(orientation='vertical', padding=dp(10))
        scroll = ScrollView()
        
        text_label = Label(
            text=self.original_content,
            font_name=FONT,
            color=COLORS['text_dark'],
            font_size='16sp',
            size_hint_y=None,
            halign='left',
            valign='top',
            text_size=(None, None)
        )
        text_label.bind(
            texture_size=text_label.setter('size'),
            width=lambda ins, w: setattr(ins, 'text_size', (w, None))
        )
        scroll.add_widget(text_label)
        content_layout.add_widget(scroll)
        
        btn_close = Button(
            text='关闭',
            font_name=FONT,
            size_hint_y=0.15,
            background_color=(0.6, 0.6, 0.6, 1)
        )
        btn_close.bind(on_press=lambda x: popup.dismiss())
        content_layout.add_widget(btn_close)
        
        popup = Popup(
            title='原文',
            title_font=FONT,
            title_color=COLORS['text_dark'],
            content=content_layout,
            size_hint=(0.9, 0.85),
            background='',
            background_color=COLORS['white']
        )
        popup.open()
    
    def go_back(self, instance):
        self.manager.current = 'main'

