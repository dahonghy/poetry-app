# ========== 数字转中文 ==========
NUM_CN = ['零', '一', '二', '三', '四', '五', '六', '七', '八', '九', '十']

def num_to_cn(n):
    """将数字转为中文"""
    if n < 0:
        return str(n)
    elif n <= 10:
        return NUM_CN[n]
    elif n < 20:
        return '十' + (NUM_CN[n - 10] if n > 10 else '')
    elif n < 100:
        tens = n // 10
        ones = n % 10
        result = NUM_CN[tens] + '十'
        if ones > 0:
            result += NUM_CN[ones]
        return result
    elif n == 100:
        return '一百'
    else:
        return str(int(n))

